print("*** Programa que calcula la suma acumulativa **+")
Variable_1 = input("Ingrese el número final: ")
Variable_1 = int(Variable_1)

Contador_1 = 0
Total = 0

while Contador_1 <= Variable_1 :
    Total += Contador_1
    Contador_1 += 1

print(f"La suma del 0 al {Variable_1} son: ")
print(Total)

